    private void btn_EntrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_EntrarMouseClicked
        // TODO add your handling code here:
        Login l=new Login();
        puesto=l.existe(txt_Usuario.getText());
        if (interfaz.equals("Ordenes")) {
            if (puesto.equals("Gerente") || puesto.equals("Empleado")) {
                ordenes.setVisible(true);
                dispose();
            }
        }
        if (interfaz.equals("AdmEmpleados")) {
            if (puesto.equals("Gerente")) {
                empleados.setVisible(true);
                dispose();
            }
        }
        if (interfaz.equals("Ingredientes")) {
            if (puesto.equals("Gerente")) {
                ingredientes.setVisible(true);
                dispose();
            }
        }
        if (interfaz.equals("Modificar")) {
            if (puesto.equals("Gerente")) {
                ordenes.setVisible(true);
                dispose();
            }
        }
        if (interfaz.equals("Pizzas")) {
            if (puesto.equals("Gerente")) {
                pizzas.setVisible(true);
                dispose();
            }
        }
        if (interfaz.equals("Refrescos")) {
            if (puesto.equals("Gerente")) {
                refresco.setVisible(true);
                dispose();
            }
        }
        if (interfaz.equals("Registro")) {
            if (puesto.equals("Gerente")) {
                registro.setVisible(true);
                dispose();
            }
        }
        
    }//GEN-LAST:event_btn_EntrarMouseClicked

